		function OpenTurnir(){
			var popup = document.getElementById ('turnir');
			popup.style.display="block";
			var main = document.getElementById ('popup_turnir');
			main.style.display="block";
		}
		function CloseTurnir(){
			var popup = document.getElementById ('turnir');
			popup.style.display="none";
			var main = document.getElementById ('popup_turnir');
			main.style.display="none";
		}